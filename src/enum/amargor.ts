export enum IBU {
    Bajo = '0-20 IBU', // Bajo amargor, típico en cervezas suaves como las de trigo o lagers ligeras.
    Moderado = '20-40 IBU', // Amargor moderado, común en Pale Ales y Amber Ales.
    Notable = '40-60 IBU', // Amargor notable, presente en cervezas como las IPAs.
    Alto = '60+ IBU', // Amargor fuerte, característico de Imperial IPAs y algunas Stouts.
}
